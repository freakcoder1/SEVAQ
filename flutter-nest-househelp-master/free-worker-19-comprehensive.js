const { Pool } = require('pg');

const pool = new Pool({
  host: 'localhost',
  port: 5432,
  database: 'sevaq_db',
  user: 'postgres',
  password: 'admin'
});

const WORKER_ID = '19';

async function freeWorker19() {
  let client;
  try {
    client = await pool.connect();
    console.log('Connected to database');

    // Step 1: Find all bookings assigned to worker 19
    console.log('\n=== STEP 1: Finding bookings assigned to worker 19 ===');
    const bookingsResult = await client.query(`
      SELECT id, "workerId", "assignedWorkerId", "assignmentState" 
      FROM "booking" 
      WHERE "workerId" = $1 OR "assignedWorkerId" = $1
    `, [WORKER_ID]);
    
    console.log(`Found ${bookingsResult.rows.length} bookings assigned to worker 19`);
    if (bookingsResult.rows.length > 0) {
      console.log('Booking IDs:', bookingsResult.rows.map(r => r.id).join(', '));
    }

    // Step 2: Clear worker 19 from ALL bookings (both workerId and assignedWorkerId)
    console.log('\n=== STEP 2: Clearing worker 19 from all bookings ===');
    const clearBookingsResult = await client.query(`
      UPDATE "booking" 
      SET "workerId" = NULL, "assignedWorkerId" = NULL, "assignmentState" = 'pending'
      WHERE "workerId" = $1 OR "assignedWorkerId" = $1
      RETURNING id
    `, [WORKER_ID]);
    console.log(`Cleared ${clearBookingsResult.rows.length} bookings`);

    // Step 3: Delete ALL slots for worker 19
    console.log('\n=== STEP 3: Deleting all slots for worker 19 ===');
    const deleteSlotsResult = await client.query(`
      DELETE FROM "slot" 
      WHERE "workerId" = $1
      RETURNING id
    `, [WORKER_ID]);
    console.log(`Deleted ${deleteSlotsResult.rows.length} slots`);

    // Step 4: Clear worker 19 from subscription assignments (if table exists)
    console.log('\n=== STEP 4: Clearing worker 19 from subscription assignments ===');
    try {
      const clearSubscriptionsResult = await client.query(`
        UPDATE "subscription"
        SET "assignedWorkerId" = NULL
        WHERE "assignedWorkerId" = $1
        RETURNING id
      `, [WORKER_ID]);
      console.log(`Cleared ${clearSubscriptionsResult.rows.length} subscription assignments`);
    } catch (err) {
      if (err.code === '42P01') {
        console.log('Skipped: "subscription" table does not exist');
      } else {
        throw err;
      }
    }

    // Step 5: Ensure worker 19 is marked as isActive = true
    console.log('\n=== STEP 5: Ensuring worker 19 is active ===');
    const activateWorkerResult = await client.query(`
      UPDATE "worker" 
      SET "isActive" = true
      WHERE id = $1
      RETURNING id, "isActive"
    `, [WORKER_ID]);
    
    if (activateWorkerResult.rows.length > 0) {
      console.log(`Worker 19 isActive set to: ${activateWorkerResult.rows[0].isActive}`);
    } else {
      console.log('WARNING: Worker 19 not found in worker table!');
    }

    // Step 6: Delete availability records that might mark worker as unavailable
    console.log('\n=== STEP 6: Deleting availability records for worker 19 ===');
    const deleteAvailabilityResult = await client.query(`
      DELETE FROM "availability" 
      WHERE "workerId" = $1
      RETURNING id
    `, [WORKER_ID]);
    console.log(`Deleted ${deleteAvailabilityResult.rows.length} availability records`);

    // VERIFICATION SUMMARY
    console.log('\n========================================');
    console.log('       VERIFICATION SUMMARY');
    console.log('========================================');

    // Check worker isActive status
    const workerStatus = await client.query(`
      SELECT id, "isActive" FROM "worker" WHERE id = $1
    `, [WORKER_ID]);
    
    if (workerStatus.rows.length > 0) {
      console.log(`Worker 19 isActive: ${workerStatus.rows[0].isActive}`);
    } else {
      console.log('Worker 19: NOT FOUND in worker table');
    }

    // Check number of slots for worker 19
    const slotsCount = await client.query(`
      SELECT COUNT(*) as count FROM "slot" WHERE "workerId" = $1
    `, [WORKER_ID]);
    console.log(`Slots for worker 19: ${slotsCount.rows[0].count} (will be regenerated by scheduler)`);

    // Check number of bookings still assigned to worker 19
    const bookingsCount = await client.query(`
      SELECT COUNT(*) as count FROM "booking" 
      WHERE "workerId" = $1 OR "assignedWorkerId" = $1
    `, [WORKER_ID]);
    console.log(`Bookings still assigned to worker 19: ${bookingsCount.rows[0].count} (should be 0)`);

    console.log('========================================');
    console.log('Worker 19 has been successfully freed!');
    console.log('========================================');

  } catch (error) {
    console.error('Error:', error.message);
    throw error;
  } finally {
    if (client) {
      client.release();
    }
    await pool.end();
  }
}

freeWorker19()
  .then(() => {
    console.log('\nScript completed successfully');
    process.exit(0);
  })
  .catch((error) => {
    console.error('\nScript failed:', error);
    process.exit(1);
  });
