/// Design token for spacing based on 8pt grid system
class AppSpacing {
  AppSpacing._();

  /// 2px - Extra extra small
  static const double xxs = 2.0;

  /// 4px - Extra small
  static const double xs = 4.0;

  /// 8px - Small
  static const double sm = 8.0;

  /// 12px - Small medium
  static const double smd = 12.0;

  /// 16px - Medium (base unit)
  static const double md = 16.0;

  /// 20px - Medium large
  static const double mlg = 20.0;

  /// 24px - Large
  static const double lg = 24.0;

  /// 32px - Extra large
  static const double xl = 32.0;

  /// 40px - Extra extra large
  static const double xxl = 40.0;

  /// 48px - Double extra large
  static const double xxxl = 48.0;

  /// 64px - Triple extra large
  static const double xxxx = 64.0;
}
