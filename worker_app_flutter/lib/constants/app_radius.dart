/// Design token for border radius
class AppRadius {
  AppRadius._();

  /// 4px - Extra small (tight corners)
  static const double xxs = 4.0;

  /// 8px - Small (buttons, chips, input fields)
  static const double sm = 8.0;

  /// 12px - Medium (cards, dialogs)
  static const double md = 12.0;

  /// 16px - Large (bottom sheets, large cards)
  static const double lg = 16.0;

  /// 20px - Extra large (modals, popups)
  static const double xl = 20.0;

  /// 24px - Double extra large
  static const double xxl = 24.0;

  /// 999px - Full circle (avatars, circular buttons)
  static const double full = 999.0;
}
